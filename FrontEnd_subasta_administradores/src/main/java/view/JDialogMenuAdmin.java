    private void registrarProducto(){
        this.jPanelCentral.remove(objPListarProducto);
        this.jPanelCentral.remove(objCerrarSubasta);
        this.jPanelCentral.remove(objPAbrirSubasta);
        this.jPanelCentral.add(objPRegistrarProducto);
        this.jPanelCentral.revalidate();
        this.jPanelCentral.repaint();
    }
    private void listarProductos() {
        this.jPanelCentral.remove(objPRegistrarProducto);
        this.jPanelCentral.remove(objCerrarSubasta);
        this.jPanelCentral.remove(objPAbrirSubasta);
        this.jPanelCentral.add(objPListarProducto);
        this.jPanelCentral.revalidate();
        this.jPanelCentral.repaint();
    }
    private void abrirSubasta() {
        this.jPanelCentral.remove(objPListarProducto);
        this.jPanelCentral.remove(objCerrarSubasta);
        this.jPanelCentral.remove(objPRegistrarProducto);
        this.jPanelCentral.add(objPAbrirSubasta);
        this.jPanelCentral.revalidate();
        this.jPanelCentral.repaint();
    }
    private void cerrarSubasta() {
        this.jPanelCentral.remove(objPListarProducto);
        this.jPanelCentral.remove(objPAbrirSubasta);
        this.jPanelCentral.remove(objPRegistrarProducto);
        this.jPanelCentral.add(objCerrarSubasta);
        this.jPanelCentral.revalidate();
        this.jPanelCentral.repaint();
    }
